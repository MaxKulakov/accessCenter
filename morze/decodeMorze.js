export function decodeMorse(morseCode) {
  var alphabet = {
    '·−' : 'а',     '−···' : 'б',     '·−−' : 'в',    '−−·' : 'г',
    '−··' : 'д',    '·' : 'е',        '···−' : 'ж',   '−−··' : 'з',
    '··' : 'и',     '·−−−' : 'й',     '−·−' : 'к',    '·−··' : 'л',
    '−−' : 'м',     '−·' : 'н',       '−−−' : 'о',    '·−−·' : 'п',
    '·−·' : 'р',    '···' : 'с',      '−' : 'т',      '··−' : 'у',
    '··−·' : 'ф',   '····' : 'х',     '−·−·' : 'ц',   '−−−·' : 'ч',
    '−−−−' : 'ш',   '−−·−' : 'щ',     '−−·−−' : 'ъ',  '−·−−' : 'ы',
    '−··−' : 'ь',   '··−··' : 'э',    '··−−' : 'ю',   '·−·−' : 'я',
    '·−·−·−' : '.', '·−·−·−' : ',',   '−−−···' : ':', '−−··−−' : '!',
    '··−··' : '?',  '−····−' : '-',   's' : ' ',

    '·−−−−' : '1',  '··−−−' : '2',    '···−−' : '3',  '····−' : '4',
    '·····' : '5',  '−····' : '6',    '−−···' : '7',  '−−−··' : '8',
    '−−−−·' : '9',  '−−−−−' : '0'
  }

  return morseCode
    .split('   ')
    .map(
      a => a
        .split(' ')
        .map(
          b => alphabet[b]
        ).join('')
    ).join(' ');
}
